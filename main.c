task main()
{                                     //Program begins, insert code within curly braces
	bool claw_isOpen = true;

	while (true)
	{

		// Move forward/backwards
		if (vexRT(Ch3)> 0 || vexRT(Ch3) < 0)
		{
			startMotor(leftMotor, vexRT(Ch3));
			startMotor(rightMotor, vexRT(Ch3));
		}

		// Stop when joystick is not active
		if (vexRT(Ch3) == 0)
		{
			stopMotor(leftMotor);
			stopMotor(rightMotor);
		}

		// Turn right
		if (vexRT(Ch4) > 0)
		{
			startMotor(leftMotor, vexRT(Ch4));
			startMotor(rightMotor, vexRT(Ch4)*-1);
		}

		// Turn left
		if (vexRT(Ch4) < 0)
		{
			startMotor(leftMotor, vexRT(Ch4));
			startMotor(rightMotor, vexRT(Ch4)*-1);
		}

		// Stop when joystick is not active
		if (vexRT(Ch4) == 0)
		{
			stopMotor(leftMotor);
			stopMotor(rightMotor);
		}

		/*
			Open/close claw
			NOTE: Claw must be CLOSED on run (don't want to break anything)
		*/
		if (vexRT(Btn8D) == 1)
		{
			if (claw_isOpen == false)
			{
				startMotor(clawMotor, 63);
				wait(1);
				stopMotor(clawMotor);
				claw_isOpen = true;
			}
			else if (claw_isOpen == true)
			{
				startMotor(clawMotor, -63);
				wait(1);
				stopMotor(clawMotor);
				claw_isOpen = false;
			}
		}

		// TODO: Add function for controlling claw joint
		if (vexRT(Ch1) < 0 || vexRT(Ch1) > 0)
		{
			startMotor(clawJoint, vexRT(Ch1));
		}

		if (vexRT(Ch1) == 0)
		{
			stopMotor(clawJoint);
		}

		// Move conveyor belt forward
		if (vexRT(Btn6D) == 1)
		{
			startMotor(rightBelt, 127);
			startMotor(leftBelt, 127);
		}

		if (vexRT(Btn6D) == 0)
		{
			stopMotor(rightBelt);
			stopMotor(leftBelt);
		}

		// Move conveyor belt backwards
		if (vexRT(Btn5D) == 1)
		{
			startMotor(rightBelt, -127);
			startMotor(leftBelt, -127);
		}

		if (vexRT(Btn5D) == 0)
		{
			stopMotor(rightBelt);
			stopMotor(leftBelt);
		}

	}



}
